//! God-King Succession Mitigation: Self-Describing Runbook + Kernel Compat Probe
//!
//! THE PROBLEM: Only one person understands how all 50 mitigations interact.
//! A single kernel update (e.g., eBPF verifier rule change in 6.15) breaks
//! something. The community can't fix it. 10,000 deployments break.
//!
//! THE FIX: Two mechanisms:
//!
//! 1. RUNBOOK GENERATOR: On startup (or via `--emit-runbook`), the daemon
//!    introspects its own active modules and emits a structured Markdown
//!    runbook describing what each module does, what it depends on, and
//!    what to do if it fails. The runbook is always in sync with the binary.
//!
//! 2. KERNEL COMPAT PROBE: Before loading any eBPF program, run a minimal
//!    probe program through the verifier. If it fails, log the exact kernel
//!    version + verifier error and enter graceful degraded mode instead of
//!    panicking. The error message tells the next maintainer exactly what broke.

