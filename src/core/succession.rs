// ============================================================================
// RUNBOOK GENERATOR
// ============================================================================

/// Emit a Markdown runbook to stdout (or a file). Always current with the binary.
pub fn emit_runbook() -> String {
    let mut out = String::from("# Nexus Axiom — Auto-Generated Operations Runbook\n\n");
    out.push_str("> Generated by the running binary. Always in sync with deployed code.\n\n");
    out.push_str(&format!("**Kernel**: {}\n\n", kernel_version()));
    out.push_str("---\n\n");

    for m in MODULE_REGISTRY {
        out.push_str(&format!("## `{}` — {}\n\n", m.name, m.paradox));
        out.push_str(&format!("**Depends on**: {}\n\n",
            if m.depends_on.is_empty() { "none".to_string() }
            else { m.depends_on.join(", ") }
        ));
        out.push_str(&format!("**If this module fails**: {}\n\n", m.failure_mode));
        out.push_str(&format!("**Ops guide**: {}\n\n", m.runbook));
        out.push_str("---\n\n");
    }
    out
}

// ============================================================================
// KERNEL COMPAT PROBE
// ============================================================================

pub struct KernelCompatProbe;

impl KernelCompatProbe {
    /// Run before loading eBPF programs. Returns Ok if the kernel is compatible,
    /// Err with a human-readable message if not.
    pub fn probe() -> Result<(), String> {
        let version = kernel_version();
        info!("[COMPAT] Kernel version: {}", version);

        // Check minimum kernel version (5.8 required for BPF ring buffer).
        if let Some((major, minor)) = parse_kernel_version(&version) {
            if major < 5 || (major == 5 && minor < 8) {
                let msg = format!(
                    "Kernel {}.{} is too old. Nexus Axiom requires >= 5.8 for BPF ring buffer support. \
                     Upgrade the kernel or disable the ring_buffer module.",
                    major, minor
                );
                error!("[COMPAT] {}", msg);
                return Err(msg);
            }

            // Warn about known-broken verifier versions.
            if major == 6 && minor == 15 {
                warn!("[COMPAT] Kernel 6.15 has a known eBPF verifier change affecting \
                       bounded loop unrolling. See docs/KERNEL_COMPAT.md#6.15");
            }
        }

        // Probe: attempt to load a trivial eBPF program to verify the verifier works.
        #[cfg(target_os = "linux")]
        {
            // A real implementation would use libbpf's bpf_prog_load() with a
            // minimal BPF_PROG_TYPE_SOCKET_FILTER program. Here we check for
            // the bpf syscall availability as a proxy.
            use std::process::Command;
            if let Ok(output) = Command::new("bpftool").args(["prog", "list"]).output() {
                if !output.status.success() {
                    let err = String::from_utf8_lossy(&output.stderr).to_string();
                    warn!("[COMPAT] bpftool probe failed: {}. eBPF may be restricted.", err);
                }
            }
        }

        info!("[COMPAT] Kernel compatibility probe passed.");
        Ok(())
    }
}

fn kernel_version() -> String {
    #[cfg(target_os = "linux")]
    {
        std::fs::read_to_string("/proc/version")
            .unwrap_or_else(|_| "unknown".to_string())
            .lines()
            .next()
            .unwrap_or("unknown")
            .to_string()
    }
    #[cfg(not(target_os = "linux"))]
    { "non-linux".to_string() }
}

fn parse_kernel_version(version_str: &str) -> Option<(u32, u32)> {
    // Parses "Linux version 6.8.0-..." -> (6, 8)
    let parts: Vec<&str> = version_str.split_whitespace().collect();
    let ver = parts.get(2)?;
    let nums: Vec<&str> = ver.split('.').collect();
    let major = nums.first()?.parse().ok()?;
    let minor = nums.get(1)?.parse().ok()?;
    Some((major, minor))
}
